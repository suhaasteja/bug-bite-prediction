# Function to traverse and extract lat-long pairs
def extract_lat_longs(coordinates):
    lat_longs = []
    for group in coordinates:
        for polygon in group:
            for point in polygon:
                lat_longs.append((point[1], point[0]))  # (lat, lon) order
    return lat_longs

